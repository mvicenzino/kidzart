// Helper function to get age group from age
export const getAgeGroup = (age) => {
    if (age <= 3) return 'toddler';
    if (age <= 5) return 'preschool';
    if (age <= 7) return 'early-elementary';
    if (age <= 9) return 'elementary';
    return 'tween';
};

